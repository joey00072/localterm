const SUFFIX_ALPHABET = "23456789abcdefghijkmnpqrstuvwxyz";
const generateSuffix = customAlphabet(SUFFIX_ALPHABET, FRIENDLY_ID_SUFFIX_LENGTH);

const pickRandomItem = <T>(items: readonly T[]): T => {
  const item = items[randomInt(items.length)];
  if (item === undefined) throw new Error("friendly_id_word_list_empty");
  return item;
};

export const generateFriendlyId = (): string =>
  `${pickRandomItem(ADJECTIVES)}-${pickRandomItem(ANIMALS)}-${generateSuffix()}`;
